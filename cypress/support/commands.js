var locatorValue = require("../../helpers/objectRepo.json");
Cypress.Commands.add('login', (username, password) => {
    cy.get(locatorValue['userName']).type(username);
    cy.get(locatorValue['password']).type(password);
    cy.get(locatorValue['loginButton']).click();
});